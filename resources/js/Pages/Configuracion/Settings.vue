                });
            }
        });
        
    } catch (error) {
        console.error('Error al guardar la configuraci√≥n:', error);
        
        // Mostrar toast de error
        toast.add({
            severity: 'error',
            summary: 'Error',
            detail: 'Error al guardar la configuraci√≥n',
            life: 5000
        });
    } finally {
        isSaving.value = false;
    }
};

